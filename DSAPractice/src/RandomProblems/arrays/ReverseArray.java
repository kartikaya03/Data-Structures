package RandomProblems.arrays;

public class ReverseArray {
    public static void main(String[] args) {
        int [] arr = {12,10,20,80,70};

        System.out.println("Reverse Array :");
        for (int i = arr.length-1; i >=0 ; i--) {

            System.out.print(" "+arr[i]);

        }

    }
}
